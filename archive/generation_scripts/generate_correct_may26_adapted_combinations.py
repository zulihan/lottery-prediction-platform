def analyze_both_sets(set1_combos, set2_combos):
    """Analyze both sets of combinations"""
    
    print(f"\nüìä COMPREHENSIVE DUAL SET ANALYSIS")
    print("=" * 50)
    
    all_combos = set1_combos + set2_combos
    
    # Score analysis
    set1_scores = [combo['score'] for combo in set1_combos]
    set2_scores = [combo['score'] for combo in set2_combos]
    
    print(f"Performance Comparison:")
    print(f"   Set 1 (May 24 Methodology): {sum(set1_scores)/len(set1_scores):.1f}/100 average")
    print(f"   Set 2 (May 26 Adapted): {sum(set2_scores)/len(set2_scores):.1f}/100 average")
    print(f"   Overall Average: {sum(set1_scores + set2_scores)/20:.1f}/100")
    
    # Range analysis for Set 2 (May 26 adapted)
    set2_numbers = []
    for combo in set2_combos:
        set2_numbers.extend(combo['numbers'])
    
    low_count = len([n for n in set2_numbers if n <= 16])
    mid_count = len([n for n in set2_numbers if 17 <= n <= 33])
    high_count = len([n for n in set2_numbers if n >= 34])
    
    print(f"\nMay 26 Adapted Set Range Distribution:")
    print(f"   Low (1-16): {low_count}/50 ({low_count/50*100:.1f}%)")
    print(f"   Mid (17-33): {mid_count}/50 ({mid_count/50*100:.1f}%)")
    print(f"   High (34-49): {high_count}/50 ({high_count/50*100:.1f}%)")
    
    # Lucky number analysis
    all_lucky = [combo['lucky'] for combo in all_combos]
    lucky_freq = {}
    for lucky in all_lucky:
        lucky_freq[lucky] = lucky_freq.get(lucky, 0) + 1
    
    print(f"\nLucky Number Distribution (All 20 combinations):")
    for lucky in sorted(lucky_freq.keys()):
        print(f"   {lucky}: {lucky_freq[lucky]} combinations")

