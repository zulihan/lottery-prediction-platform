    print("=" * 35)
    
    # Check if any of the initial 20 are exactly in the ultimate 40
    initial_20_numbers = []
    for combo in initial_10 + fusion_10:
        combo_signature = (tuple(sorted(combo['numbers'])), tuple(sorted(combo['stars'])))
        initial_20_numbers.append((combo['id'], combo_signature))
    
    ultimate_40_numbers = []
    for combo in ultimate_40:
        combo_signature = (tuple(sorted(combo['numbers'])), tuple(sorted(combo['stars'])))
        ultimate_40_numbers.append((combo['id'], combo_signature))
    
    # Find exact matches
    exact_matches = []
    for init_id, init_sig in initial_20_numbers:
        for ult_id, ult_sig in ultimate_40_numbers:
            if init_sig == ult_sig:
                exact_matches.append((init_id, ult_id))
    
    print(f"Exact matches between initial 20 and ultimate 40: {len(exact_matches)}")
    for init_id, ult_id in exact_matches:
        print(f"  {init_id} = {ult_id}")
    
    return len(exact_matches) > 0

